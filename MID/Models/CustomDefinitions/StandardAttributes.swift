import UIKit

public struct Standards {
    enum CornerRadius {
        static let fullScreenView: CGFloat = 32.0
        static let largeButton: CGFloat = 20.0
        static let formalTextField: CGFloat = 12.0
    }

    
    enum UIMotionDuration {
        static let superFast = 0.11
        static let quiteFast = 0.24
        static let fast = 0.3
        static let slow = 0.5
        static let quiteSlow = 0.6
        static let superSlow = 1.0
    }
}

